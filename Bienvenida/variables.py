# Ejemplo de variables en Python

# Declaración de variables
nombre = "Juan"        # string (cadena de texto)
edad = 25             # int (entero)
altura = 1.75         # float (decimal)
es_estudiante = True  # boolean (verdadero/falso)

# Usando las variables
print(f"Nombre: {nombre}")
print(f"Edad: {edad}")
print(f"Altura: {altura} metros")
print(f"Es estudiante: {es_estudiante}")

# Las variables pueden cambiar de valor
edad = 26
print(f"Nueva edad: {edad}")

# Operaciones con variables
a = 10
b = 5
suma = a + b
print(f"La suma de {a} + {b} = {suma}")
